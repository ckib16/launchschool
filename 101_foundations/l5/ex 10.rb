arr = [{a: 1}, {b: 2, c: 3}, {d: 4, e: 5, f: 6}]  # => [{:a=>1}, {:b=>2, :c=>3}, {:d=>4, :e=>5, :f=>6}]

new_array = arr.map do |sub_hash|      # => [{:a=>1}, {:b=>2, :c=>3}, {:d=>4, :e=>5, :f=>6}]
  sub_hash.update(sub_hash) do |_, v|  # => {:a=>1}, {:b=>2, :c=>3}, {:d=>4, :e=>5, :f=>6}
    v += 1                             # => 2, 3, 4, 5, 6, 7
  end                                  # => {:a=>2}, {:b=>3, :c=>4}, {:d=>5, :e=>6, :f=>7}
end                                    # => [{:a=>2}, {:b=>3, :c=>4}, {:d=>5, :e=>6, :f=>7}]

new_array  # => [{:a=>2}, {:b=>3, :c=>4}, {:d=>5, :e=>6, :f=>7}]
